/**
 * LESSON: Types of Comments in Java
 * Comments are ignored by the compiler and help explain code.
 *
 * This is a Javadoc comment - used to generate documentation.
 * @author Emmanuel
 * @version 1.0
 */
public class Comments {
    public static void main(String[] args) {
        // This is a single-line comment

        /*
         * This is a multi-line comment.
         * It can span multiple lines.
         * Useful for longer explanations.
         */

        System.out.println("Comments help make code readable!");

        // TIP: Use comments to explain WHY, not WHAT
        // Bad:  i++; // increment i by 1
        // Good: i++; // move to the next student in the list
    }
}
